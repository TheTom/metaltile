//! `tile init` — Scaffold a new MetalTile kernel project.
//!
//! Creates the project layout in the current directory (or `--name <dir>`):
//!
//! ```text
//! <name>/
//!   Cargo.toml          (workspace-aware package, depends on metaltile-std)
//!   src/
//!     lib.rs            (sample #[kernel] and #[bench_kernel])
//!   tile.toml           (tile CLI config with runner_binary path)
//! ```

use std::path::Path;

use crate::{
    CliError,
    InitArgs,
    term::{Color, Style, paint_stderr, paint_stdout},
};

/// `TileCommand` wrapper for `tile init`.
pub struct InitCommand<'a>(pub &'a InitArgs);

impl<'a> super::TileCommand for InitCommand<'a> {
    fn run(&self, _harness: &crate::harness::Harness) -> Result<(), crate::CliError> { run(self.0) }
}

pub fn run(args: &InitArgs) -> Result<(), CliError> {
    let name = &args.name;
    let root = Path::new(name.as_str());

    if root.exists() && !args.force {
        eprintln!(
            "{} directory '{}' already exists. Use --force to overwrite.",
            paint_stderr("error:", Style::new().fg(Color::Red).bold()),
            name,
        );
        return Err(CliError::Other(format!("'{name}' already exists")));
    }

    // Create directory tree.
    let src_dir = root.join("src");
    std::fs::create_dir_all(&src_dir).map_err(CliError::Io)?;

    // Cargo.toml
    let mt_version = env!("CARGO_PKG_VERSION");
    let cargo_toml = format!(
        r#"[package]
name = "{name}"
version = "0.1.0"
edition = "2024"

[dependencies]
metaltile = "{mt_version}"
metaltile-std = "{mt_version}"
"#
    );
    write_file(&root.join("Cargo.toml"), &cargo_toml)?;

    // src/lib.rs — sample kernel
    let lib_rs = r#"//! Sample MetalTile kernel project generated by `tile init`.
use metaltile_std::prelude::*;

/// Element-wise ReLU: out[i] = max(0, src[i]).
#[kernel]
pub fn mt_relu<T: Float>(src: &[T], out: &mut [T], n: uint) {
    let i = program_id::<0>();
    if i < n {
        out[i] = src[i].max(T::zero());
    }
}

#[bench_kernel(name = "mt_relu", dtypes = [f32, f16, bf16])]
fn bench_relu(dt: DType) -> BenchSetup {
    let n = 1 << 20;
    BenchSetup::elementwise("mt_relu", n, dt)
}
"#;
    write_file(&src_dir.join("lib.rs"), lib_rs)?;

    // tile.toml — project config
    let tile_toml = r#"# tile CLI configuration for this project.
# Override the runner binary path if `__tile_runner` is not on $PATH.
# runner_binary = "/path/to/__tile_runner"

# Verbosity: 0 = quiet, 1 = profile columns, 2 = timing columns.
verbose = 0
"#;
    write_file(&root.join("tile.toml"), tile_toml)?;

    // Success banner.
    println!("{}", paint_stdout("tile init", Style::new().fg(Color::Cyan).bold()));
    println!();
    let ck = paint_stdout("✓", Style::new().fg(Color::Green).bold());
    println!(
        "  {ck}  {}",
        paint_stdout(format!("Created project '{name}'"), Style::new().fg(Color::BrightWhite),)
    );
    println!("  {}  {}/Cargo.toml", paint_stdout("   ", Style::new()), name,);
    println!("  {}  {}/src/lib.rs", paint_stdout("   ", Style::new()), name,);
    println!("  {}  {}/tile.toml", paint_stdout("   ", Style::new()), name,);
    println!();
    println!(
        "  {}",
        paint_stdout(
            format!("cd {name} && cargo build && tile bench"),
            Style::new().fg(Color::BrightBlack),
        )
    );
    println!();

    Ok(())
}

fn write_file(path: &Path, content: &str) -> Result<(), CliError> {
    std::fs::write(path, content).map_err(CliError::Io)
}
